<?php

    // +=
    $a = 0;
    $a += 10;   // $a = $a + 10

    echo $a . "<br>";

    // -=
    $b = 0;
    $b -= 10;   // $b = $b - 10

    echo $b . "<br>";

    // *=
    $c = 5;
    $c *= 2;   // $c = $c * 2

    echo $c . "<br>";

    // /=
    $d = 5;
    $d /= 2;   // $d = $d / 2

    echo $d . "<br>";

    // %=
    $e = 5;
    $e %= 2;   // $d = $d % 2

    echo $e . "<br>";

    // utilização com variaveis
    $f = 10;
    $x = 30;

    $f += $x;   // $f = $f +$x

    echo $f . "<br>";
